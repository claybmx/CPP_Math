#pragma once

namespace math {

	signed int soma(const signed int &_n1, const signed int &_n2) {
		return _n1+_n2;
	}
	signed int subtrai(const signed int &_n1, const signed int &_n2) {
		return _n1-_n2;
	}
	signed int multiplica(const signed int &_n1, const signed int &_n2) {
		return _n1*_n2;
	}
	signed int divide(const signed int &_n1, const signed int &_n2) {
		return _n1/_n2;
	}

} // math